// ============================================================
// 显示单个字符 (6×8)
// ============================================================
void OLED_ShowChar(unsigned char x, unsigned char y, unsigned char ch) {
    unsigned char i;
    if (ch < 0x20 || ch > 0x7E) ch = 0x20;
    ch -= 0x20;
    OLED_SetCursor(y, x);
    for (i = 0; i < 6; i++)
        OLED_WriteData(F6x8[ch][i]);
}

// ============================================================
// 显示字符串 (6×8 ASCII)
// ============================================================
void OLED_ShowString(unsigned char x, unsigned char y, unsigned char *str) {
    while (*str) {
        if (x + 6 > 128) { x = 0; y++; }
        if (y > 7) return;
        OLED_ShowChar(x, y, *str++);
        x += 6;
    }
}

// ============================================================
// 显示 16 像素高的图像/汉字
//   逐列式取模，高位在下，亮点为 1
//   Image 格式: [col0上, col0下, col1上, col1下, ..., col(W-1)上, col(W-1)下]
//   y: 起始页 (0~6, 因为占 2 页)
//   x: 起始列 (0~127)
//   width: 图像宽度 (像素/列数)
// ============================================================
void OLED_ShowImage_H16(unsigned char y, unsigned char x,
                        unsigned char width, unsigned char *image) {
    unsigned char i;
    if (y <= 7) {
        OLED_SetCursor(y, x);
        for (i = 0; i < width; i++)
            OLED_WriteData(image[i * 2]);
    }
    if (y + 1 <= 7) {
        OLED_SetCursor(y + 1, x);
        for (i = 0; i < width; i++)
            OLED_WriteData(image[i * 2 + 1]);
    }
}
